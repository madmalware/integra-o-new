    private void buttonCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastroActionPerformed
        // TODO add your handling code here
        
        Usuario pessoa = new Usuario();
        pessoa.setEmail(textEmail.getText());
        pessoa.setSenha(textSenha.getText());
        pessoa.setNome(textNome.getText());
        pessoa.setCpf(textCpf.getText());
        pessoa.setConfirmar(textConfirma.getText());
        setValueIDperfil(pessoa);
        
        DatabaseConnection c = new DatabaseConnection();
       
        try {
            String senha = textSenha.getText();
            String confirma = textConfirma.getText();

            if (!confirma.equals(senha)) {
                JOptionPane.showMessageDialog(null, "Os campos 'SENHA' e 'CONFIRMAR SENHA' precisam ter os mesmos valores!");
            } else if (textEmail.getText().isEmpty() || senha.isEmpty() || textNome.getText().isEmpty() || textCpf.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            } else {
                c.createUsuario(pessoa);
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
                limparCampos();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_buttonCadastroActionPerformed

