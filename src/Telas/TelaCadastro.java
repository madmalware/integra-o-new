    public void PreencherComboPerfil(){
        DatabaseConnection c = new DatabaseConnection();

        ArrayList<Perfil> perfil= c.readPerfil();

        for (Perfil p : perfil) {
            comboPerfil.addItem(p.getPerfil());
        }
    }
        
    public void setValueIDperfil(Usuario p){
        // Montadora
        String perfilSelecionado = (String) comboPerfil.getSelectedItem();
        String query = "SELECT IDperfil FROM Perfil WHERE perfil = ?";
        Connection conn = getConnection();
        PreparedStatement sta;
        ResultSet rs;

        try {
            sta = conn.prepareStatement(query);
            sta.setString(1, perfilSelecionado);
            rs = sta.executeQuery();

            if (rs.next()) {
                idPerfil= rs.getInt("IDperfil");
                p.setIdPerfil(idPerfil);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

